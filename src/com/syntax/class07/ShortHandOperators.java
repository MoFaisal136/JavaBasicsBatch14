package com.syntax.class07;

public class ShortHandOperators {

	public static void main(String[] args) {


		int i = 10;
		
		i=i+9;
		System.out.println(i);//19
		
		i=i-5;
		System.out.println(i);//14
		
		i=i*2;
		System.out.println(i);//28
		
		int a=10;
		a+=9;
		System.out.println(a);
		
		a-=5;
		a*=2;
		a-=10;
		a%=4;
		System.out.println(a);
	}

}
