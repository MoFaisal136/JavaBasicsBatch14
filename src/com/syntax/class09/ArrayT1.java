package com.syntax.class09;

public class ArrayT1 {

	public static void main(String[] args) {

		char[] grade =  {'A', 'B', 'C', 'D', 'F'};
		
		
		
		System.out.println(grade[0]);
		
		
		
		//Retrive all elements
		
		for (int i = 0; i<grade.length; i++) {
			System.out.print(grade[i]+" ");
		}
		
		
		
	}

}
