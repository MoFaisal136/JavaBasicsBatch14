package com.syntax.class07;

public class DoWhileDemo {

	public static void main(String[] args) {

		int num=1;
		
		while (num<=3) {
			System.out.println("While");
			num++;
		}
		System.out.println("_______________________");
		
		
		int num1=1;
		
		do {
			System.out.println("Do While");
			num1++;
		}while(num1<=3);
		
		
		
	}

}
